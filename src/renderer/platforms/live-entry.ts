/** Compact number formatting shared by the platform providers' `liveEntries()`
 *  producers and the dashboard viewer cards. */
export function fmtNum(n: number): string {
  if (n >= 1_000_000) return `${(n / 1_000_000).toFixed(1)}M`;
  if (n >= 1_000) return `${(n / 1_000).toFixed(1)}k`;
  return String(n);
}
